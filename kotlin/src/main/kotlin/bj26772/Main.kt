fun main() {
    val br = BufferedReader(InputStreamReader(System.`in`))
    val n = br.readLine().toInt()
    println(" @@@   @@@  ".repeat(n))
    println("@   @ @   @ ".repeat(n))
    println("@    @    @ ".repeat(n))
    println("@         @ ".repeat(n))
    println(" @       @  ".repeat(n))
    println("  @     @   ".repeat(n))
    println("   @   @    ".repeat(n))
    println("    @ @     ".repeat(n))
    println("     @      ".repeat(n))
}
